//! Single consolidated integration-test binary for `pgmcp-testing`.
//!
//! Cargo autodiscovers every `tests/*.rs` as its own `[[test]]` target, which
//! meant 234 independent crates each statically linking the 282 MB `libpgmcp`
//! rlib plus candle/ort/cudarc — ~23.5 GB of linker output for 817 tests.
//! `autotests = false` in Cargo.toml plus this one target collapses that to a
//! single compile + link. Test names keep their file as a module prefix, so
//! filters like `cargo test documentation_guidelines` and `nextest run -E ...`
//! behave as before.
//!
//! To add a test file: drop it in `tests/` and add a `mod` line below.

