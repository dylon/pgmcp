//! Unicode lookup tables for the LaTeX → plain-text renderer.
//!
//! These map LaTeX control words (as they appear in `latex_parser`'s `MathExpr`
//! and `Node` trees) to readable Unicode. They are pure data + lookups, no
//! dependencies. Math symbol/operator coverage is a superset of what
//! `latex_parser::math_sem` can classify, so the renderer can always produce
//! something better than the raw TeX that `pandoc --to plain` dumps.

/// Greek letters and standalone math symbols (`\alpha` → `α`, `\infty` → `∞`).
pub fn math_symbol(name: &str) -> Option<&'static str> {
    Some(match name {
        // lowercase Greek
        "alpha" => "α",
        "beta" => "β",
        "gamma" => "γ",
        "delta" => "δ",
        "epsilon" | "varepsilon" => "ε",
        "zeta" => "ζ",
        "eta" => "η",
        "theta" | "vartheta" => "θ",
        "iota" => "ι",
        "kappa" => "κ",
        "lambda" => "λ",
        "mu" => "μ",
        "nu" => "ν",
        "xi" => "ξ",
        "pi" | "varpi" => "π",
        "rho" | "varrho" => "ρ",
        "sigma" | "varsigma" => "σ",
        "tau" => "τ",
        "upsilon" => "υ",
        "phi" | "varphi" => "φ",
        "chi" => "χ",
        "psi" => "ψ",
        "omega" => "ω",
        // uppercase Greek
        "Gamma" => "Γ",
        "Delta" => "Δ",
        "Theta" => "Θ",
        "Lambda" => "Λ",
        "Xi" => "Ξ",
        "Pi" => "Π",
        "Sigma" => "Σ",
        "Upsilon" => "Υ",
        "Phi" => "Φ",
        "Psi" => "Ψ",
        "Omega" => "Ω",
        // common symbols
        "infty" => "∞",
        "partial" => "∂",
        "nabla" => "∇",
        "emptyset" | "varnothing" => "∅",
        "aleph" => "ℵ",
        "hbar" => "ℏ",
        "ell" => "ℓ",
        "wp" => "℘",
        "Re" => "ℜ",
        "Im" => "ℑ",
        "angle" => "∠",
        "top" => "⊤",
        "bot" => "⊥",
        "vdash" => "⊢",
        "models" => "⊨",
        "Box" => "□",
        "Diamond" => "◇",
        "checkmark" => "✓",
        "dagger" => "†",
        "ddagger" => "‡",
        "prime" => "′",
        _ => return None,
    })
}

