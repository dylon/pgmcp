/// The combining diacritic for an accent command name (`'` → U+0301 acute,
/// `hat` → U+0302 circumflex). Applied after a base character; the downstream
/// `normalize_extracted_text` NFKC pass composes it to a precomposed glyph
/// (e.g. `e` + U+0301 → `é`) where one exists.
pub fn accent_combining(name: &str) -> Option<char> {
    Some(match name {
        // text-mode single-char accents
        "'" | "acute" => '\u{0301}',
        "`" | "grave" => '\u{0300}',
        "\"" | "ddot" => '\u{0308}',
        "^" | "hat" | "widehat" => '\u{0302}',
        "~" | "tilde" | "widetilde" => '\u{0303}',
        "=" | "bar" => '\u{0304}',
        "." | "dot" => '\u{0307}',
        "u" | "breve" => '\u{0306}',
        "v" | "check" => '\u{030C}',
        "H" => '\u{030B}',
        "c" => '\u{0327}',
        "k" => '\u{0328}',
        "r" | "mathring" => '\u{030A}',
        "b" => '\u{0331}',
        "d" => '\u{0323}',
        "vec" => '\u{20D7}',
        "overline" => '\u{0305}',
        "underline" => '\u{0332}',
        "dddot" => '\u{20DB}',
        _ => return None,
    })
}

